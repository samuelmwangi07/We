#Arithmetic Operations - Simple calculations

x = 34
y = 20

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus - Returns the remainder

#Comparison Operators - Compare Values
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) #Equal to
print(x != y) #Not equal to

#Assignment Operators
number = 10
print(number)

number += 2

#OPERATOR PRECEDENCE

number %= 2
print(number)

#logical operators - and, or, not
num1 = 60
num2 = 12

print( num1 == num2 and num1 > num2)
print( num1 == num2 or num1 > num2)
print( not (num1 == num2 or num1 > num2))

